The Five Luminous Mysteries are traditionally prayed on Thursdays.

1. Make the Sign of the Cross.
2. Pray the Apostles' Creed.
3. Pray one Our Father.
4. Pray three Hail Marys for faith, hope, and charity.
5. Pray one Glory Be.
6. The First Luminous Mystery: The Baptism of Christ in the Jordan

After Jesus was baptized, he came up from the water and behold, the heavens were opened [for him], and he saw the Spirit of God descending like a dove [and] coming upon him. And a voice came from the heavens, saying, 'This is my beloved Son, with whom I am well pleased.' - Matthew 3:16-17

Fruit of the mystery: Openness to the Holy Spirit.

7. While meditating this mystery pray the Our Father followed by 10 Hail Marys and a Glory Be. After finishing each decade, it is optional to say the following prayer requested by the Blessed Virgin Mary at Fatima: O my Jesus, forgive us our sins, save us from the fires of hell; lead all souls to Heaven, especially those who have most need of your mercy.

8. The Second Luminous Mystery: The Wedding Feast at Cana

